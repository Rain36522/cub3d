#include "../Includes/cub3d.h"

void	ft_put_array(char **a)
{
	int	i;

	if (!a)
	{
		printf("NULL\n");
		return ;
	}
	i = 0;
	while (a[i])
		printf("array : %s\n", a[i ++]);
}